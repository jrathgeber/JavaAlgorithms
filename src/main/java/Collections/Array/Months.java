package Collections.Array;

public class Months {


    public static void main(String... args) {


        int monthDays [];

        monthDays = new int[12];

        monthDays[0] = 31;
        monthDays[1] = 26;


        int x;
        for (x=0 ; x<=2; x++)
            System.out.println ("January has [" + monthDays[x] + "] days");


    }



}
